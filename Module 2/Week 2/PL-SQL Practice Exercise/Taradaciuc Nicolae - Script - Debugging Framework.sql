-- 4) BODY-UL PACHETULUI DEBUG_UTILS

CREATE OR REPLACE PACKAGE BODY debug_utils AS

    -- SANITIZE_TEXT
    --
    -- Curata textul inainte de logare:
    --   1) inlocuieste NULL cu <NULL>
    --   2) elimina newline-urile
    --   3) limiteaza textul la 4000 caractere

    FUNCTION sanitize_text(p_text IN VARCHAR2) RETURN VARCHAR2 IS
    BEGIN
        RETURN SUBSTR(
                   REPLACE(REPLACE(NVL(p_text, '<NULL>'), CHR(10), ' '), CHR(13), ' '),
                   1,
                   4000
               );
    END sanitize_text;


    -- MASK_IF_SENSITIVE
    --
    -- Daca numele variabilei pare sensibil, nu logam valoarea reala.
    -- Exemple tratate: PASS, PASSWORD, PWD, TOKEN, SECRET, API_KEY

    FUNCTION mask_if_sensitive(
        p_name  IN VARCHAR2,
        p_value IN VARCHAR2
    ) RETURN VARCHAR2 IS
        l_name VARCHAR2(4000);
    BEGIN
        l_name := UPPER(NVL(p_name, 'X'));

        IF REGEXP_LIKE(l_name, '(^|_)(PASS|PASSWORD|PWD|TOKEN|SECRET|API_KEY)(_|$)') THEN
            RETURN '[REDACTED]';
        END IF;

        RETURN sanitize_text(p_value);
    END mask_if_sensitive;

    FUNCTION detect_level_from_message(
        p_message IN VARCHAR2
    ) RETURN VARCHAR2 IS
        l_message VARCHAR2(4000) := UPPER(NVL(p_message, ''));
    BEGIN
        IF l_message LIKE '[ERROR]%' THEN
            RETURN 'ERROR';
        ELSIF l_message LIKE '[WARN]%' THEN
            RETURN 'WARN';
        ELSE
            RETURN 'INFO';
        END IF;
    END detect_level_from_message;

    FUNCTION normalize_log_level(
        p_log_level IN VARCHAR2,
        p_message   IN VARCHAR2
    ) RETURN VARCHAR2 IS
        l_log_level VARCHAR2(10);
    BEGIN
        l_log_level := UPPER(TRIM(NVL(p_log_level, detect_level_from_message(p_message))));

        IF l_log_level = 'INFO'
           OR l_log_level = 'WARN'
           OR l_log_level = 'ERROR' THEN
            RETURN l_log_level;
        END IF;

        RETURN 'INFO';
    END normalize_log_level;

    FUNCTION should_write_message(
        p_log_level IN VARCHAR2,
        p_force     IN BOOLEAN DEFAULT FALSE
    ) RETURN BOOLEAN IS
        l_level VARCHAR2(10);
    BEGIN
        IF p_force THEN
            RETURN TRUE;
        END IF;

        l_level := normalize_log_level(p_log_level, NULL);

        CASE l_level
            WHEN 'WARN' THEN
                RETURN g_warn_mode;
            WHEN 'ERROR' THEN
                RETURN g_error_mode;
            ELSE
                RETURN g_debug_mode;
        END CASE;
    END should_write_message;


    -- WRITE_LOG
    --
    -- Procedura interna care scrie efectiv in DEBUG_LOG.
    --
    -- P_FORCE = TRUE inseamna ca va scrie logul chiar daca g_debug_mode este FALSE
    --
    -- Am pus AUTONOMOUS_TRANSACTION pentru ca logurile sa ramana salvate
    -- chiar daca tranzactia principala este anulata cu ROLLBACK.

    PROCEDURE write_log(
        p_module_name IN VARCHAR2,
        p_line        IN NUMBER,
        p_message     IN VARCHAR2,
        p_force       IN BOOLEAN DEFAULT FALSE,
        p_log_level   IN VARCHAR2 DEFAULT NULL,
        p_error_code  IN NUMBER DEFAULT NULL
    ) IS
        PRAGMA AUTONOMOUS_TRANSACTION;

        l_module_name debug_log.module_name%TYPE;
        l_log_message debug_log.log_message%TYPE;
        l_log_level   debug_log.log_level%TYPE;
    BEGIN
        l_log_level := normalize_log_level(p_log_level, p_message);

        IF should_write_message(l_log_level, p_force) THEN
            l_module_name := SUBSTR(NVL(p_module_name, 'UNKNOWN_MODULE'), 1, 100);
            l_log_message := sanitize_text(p_message);

            INSERT INTO debug_log (
                module_name,
                line_no,
                log_level,
                error_code,
                log_message
            )
            VALUES (
                l_module_name,
                p_line,
                l_log_level,
                p_error_code,
                l_log_message
            );

            COMMIT;
        END IF;
    EXCEPTION
        WHEN OTHERS THEN
            DBMS_OUTPUT.PUT_LINE(
                '[DEBUG_FRAMEWORK_WARNING] write_log failed. SQLCODE=' || SQLCODE ||
                ' | SQLERRM=' || SQLERRM
            );
            ROLLBACK;
    END write_log;


    -- ENABLE_DEBUG
    --
    -- Activam debug-ul si scriem un mesaj.
    PROCEDURE enable_debug IS
    BEGIN
        g_debug_mode := TRUE;

        write_log(
            $$PLSQL_UNIT,
            $$PLSQL_LINE,
            '[INFO] Debug mode enabled.',
            TRUE,
            'INFO'
        );
    END enable_debug;



    -- DISABLE_DEBUG
    --
    -- Scriem intai mesajul si apoi dezactivam debug-ul.
    PROCEDURE disable_debug IS
    BEGIN
        write_log(
            $$PLSQL_UNIT,
            $$PLSQL_LINE,
            '[INFO] Debug mode disabled.',
            TRUE,
            'INFO'
        );

        g_debug_mode := FALSE;
    END disable_debug;

    PROCEDURE enable_warn IS
    BEGIN
        g_warn_mode := TRUE;

        write_log(
            $$PLSQL_UNIT,
            $$PLSQL_LINE,
            '[INFO] WARN level enabled.',
            TRUE,
            'INFO'
        );
    END enable_warn;

    PROCEDURE disable_warn IS
    BEGIN
        write_log(
            $$PLSQL_UNIT,
            $$PLSQL_LINE,
            '[INFO] WARN level disabled.',
            TRUE,
            'INFO'
        );

        g_warn_mode := FALSE;
    END disable_warn;

    PROCEDURE enable_error IS
    BEGIN
        g_error_mode := TRUE;

        write_log(
            $$PLSQL_UNIT,
            $$PLSQL_LINE,
            '[INFO] ERROR level enabled.',
            TRUE,
            'INFO'
        );
    END enable_error;

    PROCEDURE disable_error IS
    BEGIN
        write_log(
            $$PLSQL_UNIT,
            $$PLSQL_LINE,
            '[INFO] ERROR level disabled.',
            TRUE,
            'INFO'
        );

        g_error_mode := FALSE;
    END disable_error;

    PROCEDURE clear_session_logs IS
        PRAGMA AUTONOMOUS_TRANSACTION;
    BEGIN
        DELETE FROM debug_log
         WHERE session_id = SYS_CONTEXT('USERENV', 'SESSIONID');

        COMMIT;
    EXCEPTION
        WHEN OTHERS THEN
            DBMS_OUTPUT.PUT_LINE(
                '[DEBUG_FRAMEWORK_WARNING] clear_session_logs failed. SQLCODE=' || SQLCODE ||
                ' | SQLERRM=' || SQLERRM
            );
            ROLLBACK;
    END clear_session_logs;

    PROCEDURE purge_logs IS
        PRAGMA AUTONOMOUS_TRANSACTION;
    BEGIN
        DELETE FROM debug_log;

        COMMIT;
    EXCEPTION
        WHEN OTHERS THEN
            DBMS_OUTPUT.PUT_LINE(
                '[DEBUG_FRAMEWORK_WARNING] purge_logs failed. SQLCODE=' || SQLCODE ||
                ' | SQLERRM=' || SQLERRM
            );
            ROLLBACK;
    END purge_logs;



    -- LOG

    PROCEDURE log(
        p_module_name IN VARCHAR2,
        p_line        IN NUMBER,
        p_message     IN VARCHAR2
    ) IS
    BEGIN
        write_log(
            p_module_name,
            p_line,
            p_message
        );
    END log;


    -- LOG_MSG - varianta simpla
    --
    -- O am scrisa pentru a respecta exact logica din PDF, dar eu am partea extinsa
	-- care in opinia mea, este mai "buna"

    PROCEDURE log_msg(
        p_message IN VARCHAR2
    ) IS
    BEGIN
        write_log(
            'UNKNOWN_MODULE',
            NULL,
            '[INFO] ' || NVL(p_message, '<NULL>'),
            FALSE,
            'INFO'
        );
    END log_msg;


    -- LOG_MSG - varianta extinsa
    PROCEDURE log_msg(
        p_module_name IN VARCHAR2,
