-- 5) PROCEDURA PRINCIPALA
--
--   Pentru fiecare angajat:
--     - daca commission_pct NU este NULL -> marire cu acel procent
--     - daca commission_pct ESTE NULL    -> marire cu 2%
--
-- IMPLEMENTARE:
--   - folosim cursor explicit
--   - folosim FOR UPDATE OF salary
--   - folosim WHERE CURRENT OF pentru a actualiza exact randul curent
--   - folosim framework-ul de debug la pasii importanti:
--       citire date, calcul, actualizare

CREATE OR REPLACE PROCEDURE adjust_salaries_by_commission
IS

    -- Cursorul parcurge copia de test

    CURSOR c_employees IS
        SELECT employee_id,
               last_name,
               salary,
               commission_pct
          FROM employees_copy_for_testing
         ORDER BY employee_id
         FOR UPDATE OF salary;

    l_increase_rate NUMBER;
    l_new_salary    NUMBER;
    l_rows_updated  NUMBER;
BEGIN

    -- Ce avem aici e pe copia de test, nu pe HR.EMPLOYEES direct.
 
    debug_utils.log(
        $$PLSQL_UNIT,
        $$PLSQL_LINE,
        '[SECURITY] Procedura ruleaza doar pe EMPLOYEES_COPY_FOR_TESTING. HR.EMPLOYEES este folosit doar ca sursa pentru copiere.'
    );

    debug_utils.log_msg(
        $$PLSQL_UNIT,
        $$PLSQL_LINE,
        'Procedura de ajustare a salariilor a pornit.'
    );

    -- Parcurgem fiecare angajat.
    FOR r IN c_employees LOOP


        -- PAS 1: citim si logam datele curente

        debug_utils.log_msg(
            $$PLSQL_UNIT,
            $$PLSQL_LINE,
            'Citire date pentru angajatul curent.'
        );

        debug_utils.log_variable(
            $$PLSQL_UNIT,
            $$PLSQL_LINE,
            'employee_id',
            TO_CHAR(r.employee_id)
        );

        debug_utils.log_variable(
            $$PLSQL_UNIT,
            $$PLSQL_LINE,
            'last_name',
            r.last_name
        );

        debug_utils.log_variable(
            $$PLSQL_UNIT,
            $$PLSQL_LINE,
            'old_salary',
            TO_CHAR(r.salary, '9999999990.00')
        );

        debug_utils.log_variable(
            $$PLSQL_UNIT,
            $$PLSQL_LINE,
            'commission_pct',
            NVL(TO_CHAR(r.commission_pct), 'NULL')
        );

        IF r.commission_pct IS NOT NULL AND r.commission_pct < 0 THEN
            debug_utils.log_warn(
                $$PLSQL_UNIT,
                $$PLSQL_LINE,
                'commission_pct negativ pentru employee_id=' || r.employee_id ||
                ' (' || TO_CHAR(r.commission_pct) || '). Salariul va scadea.'
            );
        ELSIF r.commission_pct IS NOT NULL AND r.commission_pct > 0.50 THEN
            debug_utils.log_warn(
                $$PLSQL_UNIT,
                $$PLSQL_LINE,
                'commission_pct neobisnuit de mare pentru employee_id=' || r.employee_id ||
                ' (' || TO_CHAR(r.commission_pct) || '). Verifica daca valoarea este corecta.'
            );
        END IF;


        -- PAS 2: stabilim procentul de marire

        IF r.commission_pct IS NOT NULL THEN
            l_increase_rate := r.commission_pct;

            debug_utils.log_msg(
                $$PLSQL_UNIT,
                $$PLSQL_LINE,
                'commission_pct are valoare; se foloseste procentul din tabel.'
            );
        ELSE
            l_increase_rate := 0.02;

            debug_utils.log_msg(
                $$PLSQL_UNIT,
                $$PLSQL_LINE,
                'commission_pct este NULL; se foloseste marirea implicita de 2%.'
            );
        END IF;

        debug_utils.log_variable(
            $$PLSQL_UNIT,
            $$PLSQL_LINE,
            'increase_rate',
            TO_CHAR(l_increase_rate, '0.9999')
        );


        -- PAS 3: calculam noul salariu

        l_new_salary := ROUND(r.salary * (1 + l_increase_rate), 2);

        IF l_new_salary < r.salary THEN
            debug_utils.log_warn(
                $$PLSQL_UNIT,
                $$PLSQL_LINE,
                'Noul salariu este mai mic decat salariul initial pentru employee_id=' ||
                r.employee_id ||
                '. old_salary=' || TO_CHAR(r.salary, '9999999990.00') ||
                ', new_salary=' || TO_CHAR(l_new_salary, '9999999990.00')
            );
        END IF;

        debug_utils.log_msg(
            $$PLSQL_UNIT,
            $$PLSQL_LINE,
            'S-a calculat noul salariu.'
        );

        debug_utils.log_variable(
            $$PLSQL_UNIT,
            $$PLSQL_LINE,
            'new_salary',
            TO_CHAR(l_new_salary, '9999999990.00')
        );


        -- PAS 4: actualizam randul curent

        UPDATE employees_copy_for_testing
           SET salary = l_new_salary
         WHERE CURRENT OF c_employees;

        l_rows_updated := SQL%ROWCOUNT;

        debug_utils.log_msg(
            $$PLSQL_UNIT,
            $$PLSQL_LINE,
            'S-a executat instructiunea UPDATE.'
        );

        debug_utils.log_variable(
            $$PLSQL_UNIT,
            $$PLSQL_LINE,
            'rows_updated',
            TO_CHAR(l_rows_updated)
        );

  
        -- PAS 5: verificare rowcount
		
        IF l_rows_updated = 1 THEN
            debug_utils.log_msg(
                $$PLSQL_UNIT,
                $$PLSQL_LINE,
                'Actualizarea a reusit pentru angajatul curent.'
            );
        ELSE
            debug_utils.log_warn(
                $$PLSQL_UNIT,
                $$PLSQL_LINE,
                'SQL%ROWCOUNT=' || l_rows_updated ||
                ' pentru employee_id=' || r.employee_id
            );
        END IF;
    END LOOP;

    debug_utils.log_msg(
        $$PLSQL_UNIT,
        $$PLSQL_LINE,
        'Procedura de ajustare a salariilor s-a incheiat cu succes.'
    );

EXCEPTION
    WHEN OTHERS THEN

        -- Logam eroarea si backtrace-ul
		
        debug_utils.log_error(
            $$PLSQL_UNIT,
            $$PLSQL_LINE,
            SQLERRM || ' | Backtrace: ' || DBMS_UTILITY.FORMAT_ERROR_BACKTRACE,
            SQLCODE
        );

